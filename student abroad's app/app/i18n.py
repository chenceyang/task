def current_language():
    lang = session.get("lang", "ko")
    return lang if lang in LANGUAGES else "ko"

def get_translator():
    def t(key):
        lang = current_language()
        return TRANSLATIONS.get(lang, TRANSLATIONS["ko"]).get(key, key)
    return t
